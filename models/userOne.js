import { DataTypes } from "sequelize";
import { dbConnection } from "../database/connection.js";

export const userOne = dbConnection.define("userOne", {
  username: {
    type: DataTypes.STRING,
    allowNull: false,
    unique: true,
  },
  email: {
    type: DataTypes.STRING,
    allowNull: false,
    unique: true,
  },
  password: {
    type: DataTypes.STRING,
    allowNull: false,
  },
});
